const fs = require('fs');
const path = require('path');
const crypto = require('crypto');

const distDir = path.join(__dirname, '..', 'dist');
const packageJson = require(path.join(__dirname, '..', 'package.json'));
const version = packageJson.version;

function generateYml(filename, zipFiles) {
  const files = [];
  for (const zipFile of zipFiles) {
    const zipPath = path.join(distDir, zipFile);
    if (!fs.existsSync(zipPath)) {
      console.log(`  Skipping ${zipFile} — not found`);
      continue;
    }
    const buf = fs.readFileSync(zipPath);
    const sha512 = crypto.createHash('sha512').update(buf).digest('base64');
    files.push({ url: zipFile, sha512, size: buf.length });
  }

  if (files.length === 0) {
    console.log(`No zip files found for ${filename}`);
    return;
  }

  const primary = files[0];
  const yml = [
    `version: ${version}`,
    `files:`,
    ...files.map(f => `  - url: ${f.url}\n    sha512: ${f.sha512}\n    size: ${f.size}`),
    `path: ${primary.url}`,
    `sha512: ${primary.sha512}`,
    `releaseDate: ${new Date().toISOString()}`,
    ''
  ].join('\n');

  fs.writeFileSync(path.join(distDir, filename), yml);
  console.log(`Generated ${filename}`);
}

// macOS — only if electron-builder hasn't already generated it
const macFiles = [`DesktopPet-${version}-arm64-mac.zip`];
// macOS latest-mac.yml is auto-generated by electron-builder for DMG target.
// Only generate manually if it doesn't exist.
if (!fs.existsSync(path.join(distDir, 'latest-mac.yml'))) {
  generateYml('latest-mac.yml', macFiles);
} else {
  console.log('latest-mac.yml already exists (auto-generated by electron-builder), skipping.');
}

// Windows — must be generated manually since zip target doesn't auto-create it
const winFiles = [
  `DesktopPet-${version}-win.zip`,
  `DesktopPet-${version}-arm64-win.zip`
];
generateYml('latest.yml', winFiles);
